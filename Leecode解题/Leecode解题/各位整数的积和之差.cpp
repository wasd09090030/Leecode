/*给你一个整数 n，请你帮忙计算并返回该整数「各位数字之积」与「各位数字之和」的差。

 

示例 1：

输入：n = 234
输出：15 
解释：
各位数之积 = 2 * 3 * 4 = 24 
各位数之和 = 2 + 3 + 4 = 9 
结果 = 24 - 9 = 15*/


//234先对10取余得4，然后除以10再对10取余得3，再得2.

class Solution {
public:
    int subtractProductAndSum(int n) {

        int p = 1, q = 0;
        while (n != 0) {
            p *= n % 10;
            q += n % 10;
            n /= 10;
        }
        return p - q;

    }
};